// Language management
let currentLanguage = localStorage.getItem('language') || 'en';

// Make setLanguage globally accessible
window.setLanguage = function(lang) {
    currentLanguage = lang;
    localStorage.setItem('language', lang);
    document.documentElement.lang = lang;
    if (typeof translatePage === 'function') {
        translatePage();
    }
    updateLanguageButton();
};

// Make translatePage globally accessible
window.translatePage = function() {
    const page = getCurrentPage();
    const t = translations[currentLanguage];
    
    if (!t) return;
    
    // Translate elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        const keys = key.split('.');
        let value = t;
        
        for (const k of keys) {
            if (value && value[k]) {
                value = value[k];
            } else {
                value = null;
                break;
            }
        }
        
        if (value) {
            if (element.tagName === 'INPUT' && element.type === 'submit') {
                element.value = value;
            } else if (element.tagName === 'A' && element.classList.contains('btn')) {
                // Keep button styling, just update text
                const textNode = Array.from(element.childNodes).find(node => node.nodeType === 3);
                if (textNode) {
                    textNode.textContent = value;
                } else {
                    element.textContent = value;
                }
            } else {
                element.textContent = value;
            }
        }
    });
    
    // Translate placeholders
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        const keys = key.split('.');
        let value = t;
        
        for (const k of keys) {
            if (value && value[k]) {
                value = value[k];
            } else {
                value = null;
                break;
            }
        }
        
        if (value) {
            element.placeholder = value;
        }
    });
    
    // Update page title
    if (t[page] && t[page].pageTitle) {
        document.title = t[page].pageTitle + ' - EmmanEzk Church';
    }
    
    // Update HTML lang attribute
    document.documentElement.lang = currentLanguage;
};

function getCurrentPage() {
    const path = window.location.pathname;
    if (path.includes('about')) return 'about';
    if (path.includes('events')) return 'events';
    if (path.includes('sermons')) return 'sermons';
    return 'home';
}

function updateLanguageButton() {
    const langBtn = document.getElementById('lang-toggle');
    if (langBtn) {
        langBtn.textContent = currentLanguage === 'en' ? 'தமிழ்' : 'English';
        langBtn.setAttribute('data-lang', currentLanguage === 'en' ? 'ta' : 'en');
    }
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', () => {
    // Set initial language from localStorage or default to English
    const savedLang = localStorage.getItem('language') || 'en';
    currentLanguage = savedLang;
    document.documentElement.lang = currentLanguage;
    
    translatePage();
    updateLanguageButton();
    
    // Language toggle button handler
    const langBtn = document.getElementById('lang-toggle');
    if (langBtn) {
        langBtn.addEventListener('click', (e) => {
            e.preventDefault();
            const newLang = langBtn.getAttribute('data-lang');
            setLanguage(newLang);
        });
    }
});

